# + for addition
print(5 + 5)
# -  for subtraction
print(5 - 5)
# * for multiplication
print(5*5)
# ** for exponent
print(5 ** 5)
# / for division, always returns a floating point
print(5/5)
# // floor division returns whole number and gives no remainder
print(13 // 2)
# % is modular division which returns the remainder
print(13 % 2)
